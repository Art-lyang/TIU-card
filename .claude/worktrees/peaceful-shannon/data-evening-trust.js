// Part 1/3: getTrustTier + 강도윤 + 서하은

function getTrustTier(trust, charKey) {
  var v = trust && charKey in trust ? trust[charKey] : 50;
  if (v <= 35) return 'low';
  if (v <= 60) return 'mid';
  if (v <= 80) return 'high';
  return 'bond';
}

