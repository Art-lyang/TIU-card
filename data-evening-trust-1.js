// 구간: low(0~35), mid(36~60), high(61~80), bond(81~100)

function getTrustTier(trust, charKey) {
  var v = trust && charKey in trust ? trust[charKey] : 50;
  if (v <= 35) return 'low';
  if (v <= 60) return 'mid';
  if (v <= 80) return 'high';
  return 'bond';
}

